    public PreviousOrderLines(User user, Customer customer,int orderId  ) {
        
        loggedInUser = user;
        initComponents();
        
        //Creates tej Table Model
        DefaultTableModel orderLines = (DefaultTableModel)tblOrderLines.getModel();
        
        Order order = customer.getOrders().get(orderId);
        
         //for each loop through HashMap
        for(Map.Entry<Integer, OrderLine> olEntry : order.getOrderLines().entrySet())
        {
            OrderLine actualOrderLine = olEntry.getValue();
            
            Product orderedProduct = new Product();
            
            String productId = "";
            String productName = "";
            int productQuantity = 0;
            String productCost = "";
            
            if(actualOrderLine.getProduct() == null) // Product has been deleted
            {
                productId = "N/A";
                productName = "N/A";
                productQuantity = 0;
                productCost = "N/A";
            }
            else
            {
                //reads the data from the Orderline
                orderedProduct = actualOrderLine.getProduct();
                productId = String.valueOf(orderedProduct.getProductId());
                productName = orderedProduct.toString();
                productQuantity = actualOrderLine.getQuantity();
                productCost = "Â£" + String.format("%.02f", orderedProduct.getPrice());
            }
            //Displays the data on the table
            orderLines.addRow(new Object[] 
                        {productId,
                        productName,
                        productQuantity,
                        productCost}                            
                );
        }
        
        tblOrderLines.setModel(orderLines);
    }

