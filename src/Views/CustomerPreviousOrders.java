    public CustomerPreviousOrders(Customer customer) {
        loggedInCustomer = customer;
        initComponents();  
        
        
        
        //Created the Table Model
        DefaultTableModel orders = (DefaultTableModel)tblOrders.getModel();
        
         //for each loop through HashMap
        for(Map.Entry<Integer, Order> oEntry : loggedInCustomer.getOrders().entrySet())
        {
            Order actualOrder = oEntry.getValue();
            
            //if the order is complete
            if(actualOrder.getStatus().equals("Complete"))
            {
                //Displays the Order in the table
                orders.addRow(new Object[] 
                        {actualOrder.getOrderId(), 
                        new SimpleDateFormat("dd/MM/yyyy").format(actualOrder.getOrderDate()), 
                        "Â£" + String.format("%.02f", actualOrder.getOrderTotal())}                            
                );
            }
        }
        
        tblOrders.setModel(orders);
        
        
    }

