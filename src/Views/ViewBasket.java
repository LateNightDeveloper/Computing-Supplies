    public ViewBasket(Customer c) {
        loggedInCustomer = c;
        currentOrder = loggedInCustomer.findLatestOrder();
        
        initComponents();
        
        
         //Creates Model Table
         DefaultTableModel basket = (DefaultTableModel)tblBasket.getModel();
         //for each loop through HashMap
        for(Map.Entry<Integer, OrderLine> olEntry : currentOrder.getOrderLines().entrySet())
        {
            OrderLine actualOrderLine = olEntry.getValue();
            
            basket.addRow(new Object[] 
                    {actualOrderLine.getProduct().getProductId(), actualOrderLine.getProduct().getProductName(),actualOrderLine.getQuantity(),
                     actualOrderLine.getProduct().getPrice(),actualOrderLine.getLineTotal()}                            
                    );
        }
        
        tblBasket.setModel(basket);
        lblTotal.setText("£" + String.format("%.02f",currentOrder.getOrderTotal()));
  
        
    }
    

