    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
        if(tblBasket.getSelectedRow() == -1)
        {
            lblErrorMessage.setText("Error: Select A Product First");
        }
        else
        {
            DefaultTableModel basketModel = (DefaultTableModel)tblBasket.getModel();
            int ProductId = Integer.parseInt(String.valueOf(basketModel.getValueAt(tblBasket.getSelectedRow(), 0))); 
            
            currentOrder.removeOrderLine(ProductId);
            lblErrorMessage.setText("Product Removed From Basket");
            
            DefaultTableModel basket = (DefaultTableModel)tblBasket.getModel();
            basket.removeRow(tblBasket.getSelectedRow());
            
            tblBasket.setModel(basket);   
            
            lblTotal.setText("£" + String.format("%.02f",currentOrder.getOrderTotal()));
        }
    }//GEN-LAST:event_btnRemoveProductActionPerformed

